import UIKit

extension UIColor {
	static var gainsBoroGrey: UIColor {
		get {
			return UIColor(displayP3Red: 220.0/255.0, green: 220.0/255.0, blue: 220.0/255.0, alpha: 1)
		}
	}
	
	static var whiteSmoke: UIColor {
		get {
			return UIColor(displayP3Red: 245.0/255.0, green: 245.0/255.0, blue: 245.0/255.0, alpha: 1.0)
		}
	}
	
	static var linen: UIColor {
		get {
			return UIColor(displayP3Red: 240.0/255.0, green: 240.0/255.0, blue: 240.0/255.0, alpha: 1.0)
		}
	}
	
	static var almostBlack: UIColor {
		get {
			return UIColor(displayP3Red: 49.0/255.0, green: 49.0/255.0, blue: 49.0/255.0, alpha: 1.0)
		}
	}
}
